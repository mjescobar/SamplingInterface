
function actualHandles=replaceHandles(actualHandles,inputHandles)
    actualHandles.mode = inputHandles.mode;
    actualHandles.list = inputHandles.list;
    actualHandles.img = inputHandles.img;
    actualHandles.bottomBar = inputHandles.bottomBar;
    actualHandles.beforeStimulus = inputHandles.beforeStimulus;
    actualHandles.flicker = inputHandles.flicker;
    actualHandles.onlyStimulus = inputHandles.onlyStimulus;
    actualHandles.presentation = inputHandles.presentation;
    actualHandles.experiments = inputHandles.experiments;
    actualHandles.screens = inputHandles.screens;
    actualHandles.time = inputHandles.time;
    actualHandles.modify = inputHandles.modify;
end